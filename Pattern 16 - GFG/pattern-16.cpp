class Solution {
  public:
    void printTriangle(int n) {
        // code here
        for(char c='A'; c<'A'+n; c++) {
            for(int j=0; j<=c-'A'; j++)
                cout << c;
            cout << "\n";
        }
    }
};

//{ Driver Code Starts.

int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;

        Solution ob;
        ob.printTriangle(n);
    }
    return 0;
}
